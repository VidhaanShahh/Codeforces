void solve() {
    int n;
    cin>>n;
    vector<int> a(n);
    for (int &x:a) cin >> x;
    int odd=0, even=0;
    for (int x:a){
        if (x % 2 == 0) even++;
        else odd++;
    }

    if ((odd>0&&even>0) || (odd%2==1))
        cout<<"YES";
    else
        cout<<"NO";
}

